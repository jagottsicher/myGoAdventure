// decodeRoomRow decodes one row of Atari 2600 playfield data (3 bytes) into
// a 40-character string using the original TIA shiftreg bit ordering:
//
//	PF0 bits: 4,5,6,7 (lower nibble unused)
//	PF1 bits: 7,6,5,4,3,2,1,0  (MSB first)
//	PF2 bits: 0,1,2,3,4,5,6,7  (LSB first)
//
// The left half (cols 0-19) is decoded directly.
// The right half (cols 20-39) is either mirrored (same position +20)
// or reversed (col 39-cx for each left col cx).
func decodeRoomRow(pf0, pf1, pf2 byte, mirror bool) string {
	// Shift register masks for columns 0-19, matching the Atari TIA hardware
	shiftreg := [20]byte{
		// PF0: bits 4,5,6,7
		0x10, 0x20, 0x40, 0x80,
		// PF1: bits 7,6,5,4,3,2,1,0
		0x80, 0x40, 0x20, 0x10, 0x08, 0x04, 0x02, 0x01,
		// PF2: bits 0,1,2,3,4,5,6,7
		0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80,
	}

	row := [40]byte{}
	for i := range row {
		row[i] = ' '
	}

	for cx := 0; cx < 20; cx++ {
		var bit bool
		switch {
		case cx < 4:
			bit = pf0&shiftreg[cx] != 0
		case cx < 12:
			bit = pf1&shiftreg[cx] != 0
		default:
			bit = pf2&shiftreg[cx] != 0
		}
		if bit {
			row[cx] = 'X'
			if mirror {
				row[cx+20] = 'X'
			} else {
				row[39-cx] = 'X'
			}
		}
	}
	return string(row[:])
}

// decodeRoom decodes 7 rows of raw playfield bytes into a [7]string.
// Each row of the result is 40 chars wide ('X' = wall, ' ' = empty).
// Rows are ordered top-to-bottom in display order (row 0 = top).
func decodeRoom(data [7][3]byte, mirror bool) [7]string {
	var result [7]string
	// The C++ source renders rows with cy=6 at top (ypos=0) and cy=0 at bottom (ypos=6).
	// We reverse here so result[0] = top of screen (cy=6), result[6] = bottom (cy=0).
	for cy := 0; cy < 7; cy++ {
		displayRow := 6 - cy // cy=6 → row 0 (top), cy=0 → row 6 (bottom)
		result[displayRow] = decodeRoomRow(data[cy][0], data[cy][1], data[cy][2], mirror)
	}
	return result
}

